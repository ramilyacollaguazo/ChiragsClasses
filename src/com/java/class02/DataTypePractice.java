        byte num2 = 32;
        short num3 = 54;
        long num4 = 33;
        float num5 = 5.6f;
        double num6 = 48.09;

        // String we've to surround with double quotes
        String num7 = "This is my java program!!" ;

        // Characters we've to surround with double quotes
        char num8 = 'a';

        // We can store only true and false into boolean variable
